    public static synchronized void showAlertMessageToUser(Activity activity, String title, String msgToDisplay) {
        AlertDialog.Builder dlgAlert = new AlertDialog.Builder(activity, R.style.AlertDialogTheme);

        dlgAlert.setMessage(msgToDisplay);
        dlgAlert.setTitle(title);
        dlgAlert.setPositiveButton("OK", null);
        dlgAlert.setCancelable(true);
        dlgAlert.create().show();

        dlgAlert.setPositiveButton("Ok",
                new DialogInterface.OnClickListener() {
                    public void onClick(DialogInterface dialog, int which) {
                        dialog.dismiss();
                    }
                });
    }

